public class ActivityInfo {
    String image = "";
    String url = "";
    String title="";
    public String getImages() {
        return image;
    }
    public void setImages(String url) {
        this.image = url;
    }
    public String getUrl() {
        return url;
    }
    public void setUrl(String content) {
        this.url = content;
    }
    public String getTitle() {
        return title;
    }
    public void setTitle(String content) {
        this.title = content;
    }

}
